#include <stdint.h>
#include "Stm32f401.h"
#include "Err_Type.h"
#include "SYSCFG_interface.h"
#include "SYSCFG_private.h"

void SYSCFG_voidSetEXTIPort(SYSCFG_Port_t Copy_Port, SYSCFG_LineNum_t Copy_LineNum)
{
	uint8_t Local_u8RegNum= (Copy_LineNum/4);
	uint8_t Local_u8BitNum= ((Copy_LineNum/4)*4);
	SYSCFG->EXTICR[Local_u8RegNum] &= ~(0b1111<<Local_u8BitNum);
	SYSCFG->EXTICR[Local_u8RegNum] |= (Copy_Port<<Local_u8BitNum);

}
